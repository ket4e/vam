namespace UnityEngine;

/// <summary>
///   <para>Determines which type of shadows should be used.</para>
/// </summary>
public enum ShadowQuality
{
	/// <summary>
	///   <para>Disable Shadows.</para>
	/// </summary>
	Disable,
	/// <summary>
	///   <para>Hard Shadows Only.</para>
	/// </summary>
	HardOnly,
	/// <summary>
	///   <para>Hard and Soft Shadows.</para>
	/// </summary>
	All
}
